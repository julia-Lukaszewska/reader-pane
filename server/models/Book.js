//------------------------------------------------------------------
//------ Book schema definition for MongoDB 
//------------------------------------------------------------------

import mongoose from 'mongoose' 

const bookSchema = new mongoose.Schema({
  //----------------------------------------------------------------
  //------ Book title 
  //----------------------------------------------------------------
  title: {
    type: String, 
    required: true, 
  },

  //----------------------------------------------------------------
  //------ File URL 
  //----------------------------------------------------------------
  fileUrl: {
    type: String, 
    required: true, 
  },

  //----------------------------------------------------------------
  //------ Date added 
  //----------------------------------------------------------------
  addedAt: {
    type: Date, 
    default: Date.now, 
  },

  //----------------------------------------------------------------
  //------ Deletion flag 
  //----------------------------------------------------------------
  isArchived: {
    type: Boolean, 
    default: false, 
  },

  //----------------------------------------------------------------
  //------ Favorite flag 
  //----------------------------------------------------------------
  isFavorited: {
    type: Boolean,
    default: false,
  },
  //----------------------------------------------------------------
  //------ Reading progress (%) 
  //----------------------------------------------------------------
  progress: {
    type: Number, 
    default: 0, 
  },

  //----------------------------------------------------------------
  //------ Current page 
  //----------------------------------------------------------------
  currentPage: {
    type: Number, 
    default: 1, 
  },

  //----------------------------------------------------------------
  //------ Total pages 
  //----------------------------------------------------------------
  totalPages: {
    type: Number, 
    default: 1, 
  },
})

//------------------------------------------------------------------
//------ Export Book model 
//------------------------------------------------------------------

export default mongoose.model('Book', bookSchema)
